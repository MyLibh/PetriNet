#include <SFML/Graphics.hpp>

auto main() -> int
{
    sf::RenderWindow window(sf::VideoMode(800, 600), "PetriNet Visualization");
    while (window.isOpen())
    {
        sf::Event event;
        while (window.pollEvent(event))
        {
            if (event.type == sf::Event::Closed)
                window.close();
        }

        window.clear();
        window.display();
    }

    return 0;
}